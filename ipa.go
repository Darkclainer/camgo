var scriptToSubscript = map[rune]rune{
	'a': 'ₐ',
	'e': 'ₑ',
	'h': 'ₕ',
	'i': 'ᵢ',
	'j': 'ⱼ',
	'k': 'ₖ',
	'l': 'ₗ',
	'm': 'ₘ',
	'n': 'ₙ',
	'o': 'ₒ',
	'p': 'ₚ',
	'r': 'ᵣ',
	's': 'ₛ',
	't': 'ₜ',
	'u': 'ᵤ',
	'v': 'ᵥ',
	'x': 'ₓ',
}

func IpaSubscript(src string) string {
	return strings.Map(func(c rune) rune {
		return scriptToSubscript[c]
	}, src)
}

func IpaSuperscript(src string) string {
	return strings.Map(func(c rune) rune {
		return scriptToSuperscript[c]
	}, src)
}
